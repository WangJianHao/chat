package com.sen.chat.common.constant.dict;

import com.sen.chat.common.constant.IEnum;

/**
 * @description:
 * @author: sensen
 * @date: 2023/8/27 22:51
 */
public enum GroupStatusEnum implements IEnum<Integer> {
    NORMAL(0, "正常"),
    DELETE(1, "解散");

    private final Integer code;

    private final String description;

    GroupStatusEnum(Integer code, String description) {
        this.code = code;
        this.description = description;
    }


    @Override
    public Integer getCode() {
        return code;
    }

    @Override
    public String getCodeString() {
        return code.toString();
    }

    @Override
    public String getDescription() {
        return description;
    }
}
